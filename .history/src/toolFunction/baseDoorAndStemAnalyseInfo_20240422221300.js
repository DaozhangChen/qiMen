// (1）开门：
// 开加戊：财名俱得。开加乙：小财可求。开加丙：贵人印绶。开加丁：远信必至。开加已：事绪不定。
// 开加庚：道路词讼，谋为两岐。
// 开加辛：阴人道路。
// 开加壬：远行有失，注意破财。
// 开加癸：阴人失财小凶。

// （2）休门：
// 休加戊：财物和合。
// 休加乙：求谋重，不得；求轻，可得。
// 休加丙：文书和合喜庆。
// 休加丁：百讼休歇。
// 休加己：暗昧不宁，后吉。休加庚：文书词讼先结后解。休加辛：疾病迟愈，失物不得。休加壬、癸：阴人词讼牵连。

//(3）生门:
// 生加丙：主贵人印绶、婚姻、书信喜事。
// 生加丁：主词讼、婚姻、财利大吉。
// 生加己：主得贵人维持，吉。
// 生加庚：主财产争讼破产，不利。
// 生加辛：主产妇疾病，后吉。
// 生加壬：主遗失财后得，贼盗易获。生加癸：主婚姻不成，余事皆吉。